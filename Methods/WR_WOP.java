package Methods;

public class WR_WOP 
{
	    // Method to perform addition
	    public static int addition() {
	        // Define two numbers to add
	        int num1 = 5;
	        int num2 = 10;

	        // Perform addition
	        int sum = num1 + num2;

	        // Return the result
	        return sum;
	    }

	    // Main method to test the addition function
	    public static void main(String[] args) {
	        // Call the addition method and store the result
	        int result = addition();

	        // Print the result
	        System.out.println("The sum is: " + result);
	    }
	}


